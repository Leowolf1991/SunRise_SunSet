public class Coordinate {
    private double dLongtitude;
    private double dLantitude;

    Coordinate(double dLongtitude, double dLantitude) {
        this.dLantitude = dLantitude;
        this.dLongtitude = dLongtitude;
    }

    public double getLongtitude() {
        return this.dLongtitude;
    }

    public double getLantitude() {
        return this.dLantitude;
    }
}
